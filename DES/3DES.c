void CharToBinary(FILE *fp)
{
	char word;
	char arr[8];
	int count=0;
	while ((word = fgetc(fp)) != EOF) {
		printf("%c",word);
		itoa(word,arr,2);
		for(int i=1;i<8;i++){
			m[count]=0;
			m[i+count]=int(arr[i-1])-48;	
		}
		count+=8;
	}
	printf("\n");
}

