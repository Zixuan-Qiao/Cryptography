void FRK(int n,int o)		//f(R,K)
{
	for(int i=0;i<48;i++){		
		er[i]=r[E[i]-1];
	}
	for(i=0;i<48;i++){		
		er[i]=(er[i]+k[o][n][i])%2;
	}
	shrink();		
	p();			
}

void EN(int o)
{
	for(int i=0;i<64;i++){
		mc[i]=m[i];	
	}
	ip();
	for(i=0;i<32;i++){
		l[i]=m[i];
	}
	for(i=32;i<64;i++){
		r[i-32]=m[i];
	}
	for(int n=0;n<15;n++){	
		for(i=0;i<32;i++){
			l1[i]=l[i];
			l[i]=r[i];
		}
		FRK(n,o);		//f(R,K)
		for(i=0;i<32;i++){		
			r[i]=(l1[i]+r[i])%2;
		}
	}//end for15
	for(i=0;i<32;i++){	
		rc[i]=r[i];	
	}
	FRK(15,o);	
	for(i=0;i<32;i++){	
		l[i]=(l[i]+r[i])%2;
	}		
	for(i=0;i<32;i++){	//r16=r15
		r[i]=rc[i];
	}	
	for(i=0;i<32;i++){
		m[i]=l[i];
	}
	for(i=32;i<64;i++){
		m[i]=r[i-32];	
	} 
	for(i=0;i<64;i++){
		mc[i]=m[i];	
	} 
	ip1();
}

void DE(int o)
{
	for(int i=0;i<64;i++){
		mc[i]=m[i];	
	} 
	ip();
	for(i=0;i<32;i++){	
		l[i]=m[i];	//l16
		l1[i]=l[i];
	}
	for(i=32;i<64;i++){
		r[i-32]=m[i];	
		rc[i-32]=r[i-32];
	}
	FRK(15,o);
	for(i=0;i<32;i++){
		l[i]=(l[i]+r[i])%2;	//l15
	}
	for(i=0;i<32;i++){	//r15
		r[i]=rc[i];	
	}
	for(int n=14;n>=0;n--){		
		for(i=0;i<32;i++){
			rc[i]=r[i];	
		}
		for(i=0;i<32;i++){
			l1[i]=l[i];
			r[i]=l[i];
		}
		FRK(n,o);
		for(i=0;i<32;i++){
			l[i]=(rc[i]+r[i])%2;
		}
		for(i=0;i<32;i++){		
			r[i]=l1[i];
		}
	}//end for15
	for(i=0;i<32;i++){
		m[i]=l[i];
	}
	for(i=32;i<64;i++){
		m[i]=r[i-32];	
	} 
	for(i=0;i<64;i++){
		mc[i]=m[i];	
	} 
	ip1();
}

