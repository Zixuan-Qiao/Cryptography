void getK(int n)		
{
	int temp1,temp2;
	for(int i=0;i<56;i++){			
		k1[i]=k2[n][PC1[i]-1];
	}
	for(i=0;i<28;i++){
		c[i]=k1[i];
	}
	for(i=28;i<56;i++){
		d[i-28]=k1[i];
	}
	for(kcounter=0;kcounter<16;kcounter++){
		if(kcounter==0||kcounter==1||kcounter==8||kcounter==15){
			temp1=c[0];
			for(int j=0;j<27;j++){
				c[j]=c[j+1];
			}
			c[27]=temp1;
			temp1=d[0];
			for(j=0;j<27;j++){
				d[j]=d[j+1];
			}
			d[27]=temp1;
		}//end if
		else{
			temp1=c[0];
			temp2=c[1];
			for(int j=0;j<26;j++){
				c[j]=c[j+2];
			}
			c[26]=temp1;
			c[27]=temp2;
			temp1=d[0];
			temp2=d[1];
			for(j=0;j<26;j++){
				d[j]=d[j+2];
			}
			d[26]=temp1;
			d[27]=temp2;
		}//end else
		for(i=0;i<28;i++){
			k1[i]=c[i];
		}
		for(i=28;i<56;i++){
			k1[i]=d[i-28];
		}		
		for(i=0;i<48;i++){		//pc2
			k[n][kcounter][i]=k1[PC2[i]-1];
		}
	}//end FORcounter
}

